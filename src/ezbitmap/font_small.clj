(defn unsigned-byte [x]
  (byte (if (> x 127)
          (- x 256)
          x)))

(defn base64-font-file []
  (doseq [[k v] font]
    (when (or (not= (count v) 5) (not (every? (partial = 5) (map count v))))
      (print "broken font at char #" k)))
  (spit "font.base64"
        (String. (.encode (Base64/getEncoder)
                          (byte-array (for [c (apply concat
                                                     (mapcat (fn [n]
                                                               (or (font n) (font 32)))
                                                             (range 32 128)))]
                                        (if (= c \space)
                                          (unsigned-byte 255)
                                          (unsigned-byte 254)))))
                 "UTF-8")))

;;(base64-font-file)
